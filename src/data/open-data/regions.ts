/**
 * Lookup map: lowercase location string -> { regionId, regionName }.
 * Use this to normalize raw location values from billing data.
 */
export const regionLookup = new Map(
  regionAliases.map((r) => [r.originalValue.toLowerCase(), { regionId: r.regionId, regionName: r.regionName }])
);

/**
 * Deduplicated list of unique regions (regionId -> regionName).
 */
export const regions: { regionId: string; regionName: string }[] = [
  ...new Map(regionAliases.map((r) => [r.regionId, r.regionName])).entries(),
].map(([regionId, regionName]) => ({ regionId, regionName }));

/**
 * Normalize a raw location string to a standard RegionId and RegionName.
 */
export function normalizeRegion(rawLocation: string): { regionId: string; regionName: string } | undefined {
  return regionLookup.get(rawLocation.toLowerCase().trim());
}
